#CREAMOS LA CLASE 
class Calculadora: 
#CARACTERISTICAS DE CALCULADORA
    def __init__(self):
        pass

    def sumar(self, num_1,num_2):
        return num_1 + num_2
    
    def restar(self, num_1,num_2): 
        return num_1 - num_2 
    
    def multiplicacion(self, num_1,num_2):
        return num_1 * num_2 
    
    def dividir(self, num_1, num_2):
        return num_1 / num_2